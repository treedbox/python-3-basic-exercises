"""Class building example."""


class calculator:
    """Calculate basic operations."""

    def addiction(x, y):
        """Adiction."""
        added = x + y
        print(added)

    def subtraction(x, y):
        """Subtraction."""
        subtracted = x - y
        print(subtracted)

    def multiplication(x, y):
        """Multiplication."""
        multiplied = x * y
        print(multiplied)

    def division(x, y):
        """Division."""
        divided = x / y
        print(divided)


calculator.addiction(3, 2)
#
calculator.subtraction(3, 2)
#
calculator.multiplication(3, 2)
#
calculator.division(3, 2)
